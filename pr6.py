# to evaluate all the assignment operators 

x = int(input("Enter x value: "))

x += 5
print("x += 5 is:", x)

x -= 3
print("x -= 3 is:", x)

x *= 2
print("x *= 2 is:", x)

x /= 2
print("x /= 2 is:", x)

x %= 3
print("x %= 3 is:", x)

x //= 2
print("x //= 2 is:", x)

x **= 2
print("x **= 2 is:", x)

x &= 3
print("x &= 3 is:", x)

x |= 2
print("x |= 2 is:", x)

x ^= 1
print("x ^= 1 is:", x)

x <<= 1
print("x <<= 1 is:", x)

x >>= 1
print("x >>= 1 is:", x)

